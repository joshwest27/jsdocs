/**
 * Calculator module - See {@tutorial calculator-tutorial}
 * @module calculator
 */

/**
 * Add two numbers
 * @param {number} firstNum - First number
 * @param {number} secondNum - Second number
 * @returns {number} - Sum of firstNum & secondNum
 */
exports.add = (firstNum, secondNum) => firstNum + secondNum;

/**
 * Multiply two numbers
 * @param {number} firstNum - First number
 * @param {number} secondNum - Second number
 * @returns {number} - Product of firstNum & secondNum
 */
exports.multiply = (firstNum, secondNum) => firstNum * secondNum;

/**
 * Subtract two numbers
 * @param {number} firstNum - First number
 * @param {number} secondNum - Second number
 * @returns {number} - Difference of firstNum & secondNum
 */
exports.subtract = (firstNum, secondNum) => firstNum - secondNum;

/**
 * Divide two numbers
 * @param {number} firstNum - First number
 * @param {number} secondNum - Second number
 * @returns {number} - Quotent of firstNum & secondNum
 */
exports.divide = (firstNum, secondNum) => firstNum / secondNum;
